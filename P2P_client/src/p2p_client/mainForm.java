    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(jList2.isSelectionEmpty())
            return;
        
        String[] location = jList2.getSelectedValue().split(":");
        int port;
        try {
            if(location.length!=3)
                throw new Exception();
            port = Integer.parseInt(location[1]);
        } catch (Exception e) {
            System.err.println("Response from server was not correctly formatted");
            return;
        }
        
        fileDownload fd = new fileDownload(location[0], port, location[2]);
        fd.start();
        
        downloadUpdater du = new downloadUpdater(fd, downloadingModel, 1000);
        du.start();
    }//GEN-LAST:event_jButton4ActionPerformed

