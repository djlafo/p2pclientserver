    private void jCheckBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBox1ActionPerformed
        // TODO add your handling code here:
        jSpinner2.setEnabled(!jCheckBox1.isSelected());
        jTextField3.setEnabled(!jCheckBox1.isSelected());
        jSpinner3.setEnabled(!jCheckBox1.isSelected());
        jCheckBox2.setEnabled((!jCheckBox1.isSelected()));
        
        jButton1.setEnabled(jCheckBox1.isSelected());
        jList1.setEnabled(jCheckBox1.isSelected());
        
        if(!jCheckBox1.isSelected()) {
            fhs.stopServer();
            fhs = null;
            hostModel.clear();
        } else {
            fhs = new fileHostServer((Integer) jSpinner2.getValue(),  jTextField3.getText(), (Integer) jSpinner3.getValue());
            fhs.start();
            if(jCheckBox2.isSelected()) {
                List<File> l = flh.loadFiles();
                for(File f : l) {
                    if(fhs.addFile(f))
                        hostModel.addElement(f.getName());
                }
            }
        }
    }//GEN-LAST:event_jCheckBox1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (jFileChooser1.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            if(!hostModel.contains(jFileChooser1.getSelectedFile().getName())) {
                if(fhs.addFile(jFileChooser1.getSelectedFile())) {
                    flh.addFile(jFileChooser1.getSelectedFile());
                    hostModel.addElement(jFileChooser1.getSelectedFile().getName());
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

